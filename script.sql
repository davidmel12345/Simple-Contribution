
CREATE TABLE dim_customer (
   customer_key      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   customer_id       NUMBER NOT NULL,
   first_name        VARCHAR2(100),
   last_name         VARCHAR2(100),
   gender            VARCHAR2(20),
   email             VARCHAR2(200),
   phone             VARCHAR2(50),
   city              VARCHAR2(100),
   state             VARCHAR2(100),
   country           VARCHAR2(100),
   postal_code       VARCHAR2(20),
   date_of_birth     DATE,
   registration_date DATE,
   record_hash       VARCHAR2(64),
   valid_from        DATE,
   valid_to          DATE,
   is_current        NUMBER(1)
);


CREATE TABLE dim_product (
   product_key     NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
   product_id      NUMBER NOT NULL,
   product_name    VARCHAR2(200),
   sku             VARCHAR2(50),
   description     VARCHAR2(1000),
   price           NUMBER(10,2),
   stock_quantity  NUMBER(10),
   product_status  VARCHAR2(20),
   category_id     NUMBER,
   supplier_id     NUMBER,
   record_hash     VARCHAR2(64),
   valid_from      DATE NOT NULL,
   valid_to        DATE,
   is_current      NUMBER(1) DEFAULT 1
);


CREATE TABLE dim_category (
   category_key    NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
   category_id     NUMBER NOT NULL,
   category_name   VARCHAR2(50),
   description     VARCHAR2(500),
   record_hash     VARCHAR2(64),
   valid_from      DATE NOT NULL,
   valid_to        DATE,
   is_current      NUMBER(1) DEFAULT 1
);


CREATE TABLE dim_supplier (
   supplier_key    NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
   supplier_id     NUMBER NOT NULL,
   supplier_name   VARCHAR2(150),
   contact_name    VARCHAR2(100),
   email           VARCHAR2(150),
   phone           VARCHAR2(20),
   city            VARCHAR2(100),
   state           VARCHAR2(50),
   country         VARCHAR2(50),
   postal_code     VARCHAR2(20),
   record_hash     VARCHAR2(64),
   valid_from      DATE NOT NULL,
   valid_to        DATE,
   is_current      NUMBER(1) DEFAULT 1
);


CREATE TABLE dim_date (
   date_key        NUMBER PRIMARY KEY,
   full_date       DATE NOT NULL,
   year            NUMBER,
   quarter         NUMBER,
   month           NUMBER,
   day_of_month    NUMBER,
   day_of_week     NUMBER,
   month_name      VARCHAR2(20),
   day_name        VARCHAR2(20)
);


